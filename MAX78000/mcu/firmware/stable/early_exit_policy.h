static inline uint16_t early_exit_threshold_for(int prefix, int class_id)
{
    for (int i = 0; i < EARLY_EXIT_PREFIX_COUNT; i++) {
        if (early_exit_prefixes[i] == prefix) {
            return early_exit_thresholds[i][class_id];
        }
    }

    return EARLY_EXIT_DISABLED_THRESHOLD;
}

