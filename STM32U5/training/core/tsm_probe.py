"""Day 8 TSM deployment-path probe.

Builds a minimal "streaming student" with the exact A.streaming interface
the Plan requires:
    inputs:  frame_t [1, 1, H, W, 1], cache_0 [1, 1, H', W', n_shift_0], cache_1 [1, 1, H'', W'', n_shift_1]
    outputs: logits_t [1, 27], cache_0_out, cache_1_out

Then runs three checks:

  Check 1 (Quantization):  Convert FP32 -> INT8 TFLite via tfmot post-training
                            quantization. Compare FP32 Keras vs INT8 TFLite
                            interpreter outputs on 32 random samples. Per the
                            Plan we want **mean KL(softmax(fp32), softmax(int8)) < 0.05**
                            (output-side KL; cache-side is monitored but not gated).

  Check 2 (X-CUBE-AI):     Run `stedgeai analyze` against the INT8 TFLite,
                            target STM32U5 (Cortex-M33 + Helium). Extract:
                            (a) whether multi-input/multi-output topology was
                            accepted (no operator errors); (b) MACs; (c) RAM /
                            ROM estimates; (d) any unsupported ops.

  Check 3 (U5 board):      Skipped — no board available on HPC. Latency is
                            extrapolated from stedgeai analyze's MACs estimate
                            using the Plan's 300 MMAC/s + 5ms/ai_run formula.

Output: prints summary + writes JSON report to `logs/tsm_probe_results.json`.
A markdown report (`tsm_probe_results.md`) is generated by a second pass.

Key design notes:
  * Probe model is **intentionally tiny** (channels 8 -> 16 -> 32, depth 2 conv
    blocks, 32x32 input — even tinier than V0). This is a SHAPE/OPS-LEVEL test:
    the point isn't accuracy, but whether the I/O topology + TSM Slice/Concat
    pattern survives quantize + stedgeai analyze.
  * Channels are chosen so n_shift = C/8 is integer at every TSM boundary
    (8 -> 1, 16 -> 2, 32 -> 4); avoids ambiguous Slice axes
  * BatchNorm folded into Conv at TFLite conversion time, ReLU as separate
    activation -- matches Plan Day 8 "Conv -> BN -> ReLU || TSM-shift" cut rule
"""

